package com.Prudhvi.Practice;

public class Alphabets {
    public static void main(String[] args) {
        System.out.println("Lowercase Alphabets: ");
        for(char ch = 'a'; ch <='z'; ch++){
            System.out.print(ch + ",");
        }
        System.out.println(" ");
        for(char ch = 'A'; ch <= 'Z'; ch++){
            System.out.print(ch + ",");
        }
    }
}
